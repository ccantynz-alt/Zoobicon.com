function generateStaticProject(
  html: string,
  projectName: string
): Record<string, string> {
  return {
    "index.html": html,
    "README.md": generateStaticReadme(projectName),
    "package.json": JSON.stringify(
      {
        name: projectName,
        version: "1.0.0",
        description: `${projectName} — generated by Zoobicon`,
        scripts: {
          dev: "npx http-server . -p 3000 -o",
          start: "npx http-server . -p 3000",
        },
        devDependencies: {
          "http-server": "^14.1.1",
        },
      },
      null,
      2
    ),
    ".gitignore": [
      "node_modules",
      ".DS_Store",
      "Thumbs.db",
      ".env",
      ".env.local",
      "",
    ].join("\n"),
    "netlify.toml": [
      "[build]",
      '  publish = "."',
      "",
      "[[redirects]]",
      '  from = "/*"',
      '  to = "/index.html"',
      "  status = 200",
      "",
    ].join("\n"),
    "vercel.json": JSON.stringify(
      {
        rewrites: [{ source: "/(.*)", destination: "/index.html" }],
      },
      null,
      2
    ),
  };
}

