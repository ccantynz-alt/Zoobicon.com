import { NextRequest, NextResponse } from "next/server";

export async function POST(req: NextRequest) {
  try {
    const { html, name, type = "static" } = await req.json();

    if (!html || typeof html !== "string") {
      return NextResponse.json(
        { error: "HTML code is required" },
        { status: 400 }
      );
    }

    if (type !== "static" && type !== "nextjs") {
      return NextResponse.json(
        { error: 'Invalid type. Must be "static" or "nextjs"' },
        { status: 400 }
      );
    }

    const projectName = sanitizeProjectName(name || "zoobicon-site");

    const files: Record<string, string> =
      type === "nextjs"
        ? generateNextJsProject(html, projectName)
        : generateStaticProject(html, projectName);

    return NextResponse.json({ files, projectName });
  } catch (error) {
    console.error("GitHub export error:", error);
    return NextResponse.json(
      { error: "Failed to generate project files" },
      { status: 500 }
    );
  }
}

function sanitizeProjectName(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    || "zoobicon-site";
}

