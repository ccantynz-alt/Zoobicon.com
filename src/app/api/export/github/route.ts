// ---------------------------------------------------------------------------
// HTML helpers
// ---------------------------------------------------------------------------

function extractFromHtml(html: string): {
  styles: string;
  bodyContent: string;
} {
  // Extract all <style> tag contents
  const styleRegex = /<style[^>]*>([\s\S]*?)<\/style>/gi;
  const styleBlocks: string[] = [];
  let match;
  while ((match = styleRegex.exec(html)) !== null) {
    styleBlocks.push(match[1].trim());
  }
  const styles = styleBlocks.join("\n\n");

  // Extract <body> content, falling back to the full HTML
  const bodyMatch = html.match(/<body[^>]*>([\s\S]*?)<\/body>/i);
  let bodyContent = bodyMatch ? bodyMatch[1].trim() : html;

  // Strip <script> tags from body for the React component
  bodyContent = bodyContent.replace(/<script[^>]*>[\s\S]*?<\/script>/gi, "");

  return { styles, bodyContent };
}

function escapeTemplateLiteral(str: string): string {
  return str
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");
}
