function generateReactProject(
  reactFiles: Record<string, string>,
  dependencies: Record<string, string>,
  projectName: string
): Record<string, string> {
  const files: Record<string, string> = {};

  // Package.json with all dependencies
  files["package.json"] = JSON.stringify({
    name: projectName,
    version: "1.0.0",
    private: true,
    scripts: {
      dev: "next dev",
      build: "next build",
      start: "next start",
      lint: "next lint",
    },
    dependencies: {
      next: "^15.0.0",
      react: "^19.0.0",
      "react-dom": "^19.0.0",
      "lucide-react": "latest",
      ...dependencies,
    },
    devDependencies: {
      typescript: "^5.6.0",
      "@types/node": "^22.0.0",
      "@types/react": "^19.0.0",
      "@types/react-dom": "^19.0.0",
      tailwindcss: "^3.4.0",
      postcss: "^8.4.0",
      autoprefixer: "^10.4.0",
    },
  }, null, 2);

  // Next.js config
  files["next.config.js"] = `/** @type {import('next').NextConfig} */
const nextConfig = {};
module.exports = nextConfig;
`;

  // TypeScript config
  files["tsconfig.json"] = JSON.stringify({
    compilerOptions: {
      target: "es2017",
      lib: ["dom", "dom.iterable", "esnext"],
      allowJs: true,
      skipLibCheck: true,
      strict: true,
      noEmit: true,
      esModuleInterop: true,
      module: "esnext",
      moduleResolution: "bundler",
      resolveJsonModule: true,
      isolatedModules: true,
      jsx: "preserve",
      incremental: true,
      plugins: [{ name: "next" }],
      paths: { "@/*": ["./src/*"] },
    },
    include: ["next-env.d.ts", "**/*.ts", "**/*.tsx", ".next/types/**/*.ts"],
    exclude: ["node_modules"],
  }, null, 2);

  // Tailwind config
  files["tailwind.config.ts"] = `import type { Config } from "tailwindcss";
const config: Config = {
  content: ["./src/**/*.{js,ts,jsx,tsx,mdx}"],
  theme: { extend: {} },
  plugins: [],
};
export default config;
`;

  files["postcss.config.js"] = `module.exports = { plugins: { tailwindcss: {}, autoprefixer: {} } };
`;

  // Global CSS
  files["src/app/globals.css"] = `@tailwind base;
@tailwind components;
@tailwind utilities;
`;

  // Layout
  files["src/app/layout.tsx"] = `import type { Metadata } from "next";
import "./globals.css";

export const metadata: Metadata = {
  title: "${projectName}",
  description: "Generated by Zoobicon — zoobicon.com",
};

export default function RootLayout({ children }: { children: React.ReactNode }) {
  return (
    <html lang="en">
      <body>{children}</body>
    </html>
  );
}
`;

  // Map generated React files into the Next.js src structure
  // App.tsx becomes the main page
  if (reactFiles["App.tsx"]) {
    // Convert App.tsx to a Next.js page
    let appCode = reactFiles["App.tsx"];
    // Replace "export default function App" with page export
    appCode = appCode.replace(
      /export default function App/,
      "export default function Page"
    );
    // Fix component imports to use relative paths
    appCode = appCode.replace(
      /from ["']\.\/components\//g,
      'from "@/components/'
    );
    files["src/app/page.tsx"] = appCode;
  }

  // Copy all other component files
  for (const [path, content] of Object.entries(reactFiles)) {
    if (path === "App.tsx") continue; // Already handled
    if (path.startsWith("components/")) {
      files[`src/${path}`] = content;
    } else if (path.endsWith(".css")) {
      files[`src/app/${path}`] = content;
    } else {
      files[`src/${path}`] = content;
    }
  }

  // Git files
  files[".gitignore"] = "node_modules\n.next\nout\n.DS_Store\n.env\n.env.local\n";

  files["README.md"] = `# ${projectName}

A React/Next.js project generated by [Zoobicon](https://zoobicon.com).

## Getting Started

\`\`\`bash
npm install
npm run dev
\`\`\`

Open [http://localhost:3000](http://localhost:3000).

## Deploy to Vercel

[![Deploy with Vercel](https://vercel.com/button)](https://vercel.com/new)

## Built With

- [Next.js 15](https://nextjs.org)
- [React 19](https://react.dev)
- [Tailwind CSS](https://tailwindcss.com)
- [TypeScript](https://typescriptlang.org)

Generated by [Zoobicon](https://zoobicon.com) — zoobicon.com · zoobicon.ai · zoobicon.io · zoobicon.sh
`;

  return files;
}
