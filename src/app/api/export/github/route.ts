import { NextRequest, NextResponse } from "next/server";

export async function POST(req: NextRequest) {
  try {
    const body = await req.json();
    const { html, reactFiles, reactDeps, name, type = "react" } = body;

    // React project export (new default)
    if (type === "react" && reactFiles && typeof reactFiles === "object") {
      const projectName = sanitizeProjectName(name || "zoobicon-app");
      const files = generateReactProject(reactFiles, reactDeps || {}, projectName);
      return NextResponse.json({ files, projectName, type: "react" });
    }

    // Legacy HTML export (kept for backwards compatibility)
    if (!html || typeof html !== "string") {
      return NextResponse.json({ error: "Code is required (reactFiles or html)" }, { status: 400 });
    }

    const projectName = sanitizeProjectName(name || "zoobicon-site");
    const files = type === "nextjs"
      ? generateNextJsProject(html, projectName)
      : generateStaticProject(html, projectName);

    return NextResponse.json({ files, projectName });
  } catch (error) {
    console.error("GitHub export error:", error);
    return NextResponse.json({ error: "Failed to generate project files" }, { status: 500 });
  }
}

function sanitizeProjectName(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    || "zoobicon-site";
}

