// ---------------------------------------------------------------------------
// Stub generation — asks Claude Haiku for N plausible-looking expiring names,
// then runs them through the shared valuation heuristic. Deterministic enough
// on a 10-min cache that we don't hammer Anthropic.
// ---------------------------------------------------------------------------

async function generateStubDrops(tld: string, count: number): Promise<DropItem[]> {
  const system =
    "You generate realistic-looking expiring domain lists. Output JSON arrays only, no prose, no markdown.";

  const user = `Generate exactly ${count} realistic expiring .${tld} domain names for a domain-aftermarket surface.

Each name should be:
  • 4-8 characters (SLD only, no TLD)
  • Letters only — no numbers, no hyphens
  • Brandable (pronounceable, memorable)
  • Mix styles: invented coinages, two-root compounds, rare Latin/Greek stems
  • Avoid obvious squatted names (skip: apex, nova, lumen, pulse, forge, atlas, phoenix, echo)

Distribute across drop windows:
  • Roughly 20% dropping in 1-2 days (status: "pendingDelete" or "dropping")
  • Roughly 40% in 8-14 days (status: "redemption")
  • Roughly 40% in 20-30 days (status: "grace")

Output JSON array only. Shape:
[
  { "sld": "vexion", "days": 2, "status": "dropping" },
  { "sld": "lumeris", "days": 12, "status": "redemption" },
  ...
]

Rules:
  • Return exactly ${count} entries.
  • "status" ∈ {"grace","redemption","pendingDelete","dropping"}
  • "days" is a positive integer.
  • No duplicates.
  • No TLD in the sld value.
  • No markdown fences, no commentary. JSON array only.`;

  let raw: Array<{ sld?: unknown; days?: unknown; status?: unknown }> | null = null;
  try {
    const { text } = await callLLMWithFailover({
      model: "claude-haiku-4-5-20251001",
      system,
      userMessage: user,
      maxTokens: 2000,
    });
    raw = extractJsonArray<{ sld?: unknown; days?: unknown; status?: unknown }>(text);
  } catch (err) {
    console.error(
      `[domains/drops] Stub LLM call failed for .${tld}:`,
      err instanceof Error ? err.message : err,
    );
  }

  if (!raw || raw.length === 0) {
    console.warn(
      `[domains/drops] No LLM stub list for .${tld} — returning a tiny deterministic seed so UI still renders.`,
    );
    return DETERMINISTIC_SEED.map((s) => buildDropItem(s.sld, tld, s.days, s.status));
  }

  const out: DropItem[] = [];
  const seen = new Set<string>();
  for (const row of raw) {
    if (!row || typeof row !== "object") continue;
    const sld =
      typeof row.sld === "string"
        ? row.sld.trim().toLowerCase().replace(/[^a-z]/g, "").slice(0, 20)
        : "";
    if (!sld || sld.length < 3) continue;
    if (seen.has(sld)) continue;

    const days = typeof row.days === "number" && Number.isFinite(row.days)
      ? Math.max(1, Math.min(Math.round(row.days), 60))
      : 14;
    const status = normaliseStatus(row.status, days);

    seen.add(sld);
    out.push(buildDropItem(sld, tld, days, status));
  }

  return out;
}

// Last-ditch seed so the endpoint never returns an empty drops array when
// Claude is down AND there's no live feed.
const DETERMINISTIC_SEED: Array<{ sld: string; days: number; status: DropStatus }> = [
  { sld: "veridax", days: 3, status: "dropping" },
  { sld: "octavus", days: 9, status: "redemption" },
  { sld: "sonare", days: 14, status: "redemption" },
  { sld: "calidus", days: 22, status: "grace" },
  { sld: "lumeris", days: 27, status: "grace" },
];

function buildDropItem(sld: string, tld: string, days: number, status: DropStatus): DropItem {
  const domain = `${sld}.${tld}`;
  const h = valueHeuristic(domain);
  const expiresAt = isoDaysFromNow(days);
  const brandability = scoreBrandability(sld, tld, h);
  const reason = buildReasonSurfaced(sld, tld, h, status, days);

  return {
    domain,
    expires_at: expiresAt,
    days_until_drop: days,
    status,
    predicted_value: { low: h.low, high: h.high, midpoint: h.midpoint },
    brandability_score: brandability,
    reason_surfaced: reason,
  };
}

function scoreBrandability(
  sld: string,
  tld: string,
  h: ReturnType<typeof valueHeuristic>,
): number {
  // Brandability 0-100 — anchored off the midpoint valuation but bounded so
  // obscure $200 names don't show as "1/100".
  let score = 40;
  if (sld.length <= 5) score += 20;
  else if (sld.length <= 7) score += 10;
  if (tld === "com") score += 15;
  else if (tld === "ai" || tld === "io") score += 10;
  if (/^[aeiou]/.test(sld) && /[aeiou]$/.test(sld)) score += 5;
  if (h.midpoint >= 1000) score += 10;
  else if (h.midpoint >= 500) score += 5;
  if (sld.includes("-") || /\d/.test(sld)) score -= 15;
  return Math.max(10, Math.min(99, score));
}

function buildReasonSurfaced(
  sld: string,
  tld: string,
  h: ReturnType<typeof valueHeuristic>,
  status: DropStatus,
  days: number,
): string {
  const chunks: string[] = [];
  if (sld.length <= 5) chunks.push(`${sld.length}-char`);
  if (tld === "com") chunks.push(".com");
  else chunks.push(`.${tld}`);
  if (h.midpoint >= 1000) chunks.push("high-value");
  if (status === "pendingDelete" || status === "dropping") {
    chunks.push(`drops in ${days}d`);
  }
  return chunks.join(", ") || `${tld} drop`;
}

