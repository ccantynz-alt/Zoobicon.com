type DropStatus = "grace" | "redemption" | "pendingDelete" | "dropping";

interface DropItem {
  domain: string;
  expires_at: string;
  days_until_drop: number;
  status: DropStatus;
  predicted_value: { low: number; high: number; midpoint: number };
  brandability_score: number;
  reason_surfaced: string;
}

interface DropsResponse {
  tld: string;
  count: number;
  drops: DropItem[];
  source: "stub" | "live";
  notes: string | null;
}

const CACHE = new LruCache<DropsResponse>(200, 10 * 60 * 1000);

const ALLOWED_TLDS = new Set([
  "com",
  "net",
  "org",
  "io",
  "ai",
  "co",
  "app",
  "dev",
  "sh",
  "me",
  "xyz",
  "us",
]);

export async function GET(req: NextRequest) {
  try {
    const tldRaw = (req.nextUrl.searchParams.get("tld") || "com").toLowerCase().replace(/^\./, "");
    const limitRaw = req.nextUrl.searchParams.get("limit");
    const minValueRaw = req.nextUrl.searchParams.get("min_value");

    if (!ALLOWED_TLDS.has(tldRaw)) {
      return NextResponse.json(
        {
          error: `Unsupported TLD "${tldRaw}". Supported: ${Array.from(ALLOWED_TLDS).join(", ")}`,
        },
        { status: 400 },
      );
    }

    const limit = clampInt(limitRaw, 20, 1, 50);
    const minValue = minValueRaw ? clampInt(minValueRaw, 0, 0, 1_000_000) : 0;

    const cacheKey = `${tldRaw}|${limit}|${minValue}`;
    const cached = CACHE.get(cacheKey);
    if (cached) {
      return NextResponse.json({ ...cached, cached: true });
    }

    // ── Live feed hook — plugs in here once a paid provider is wired ──────
    const live = await fetchDropsLive(tldRaw);
    if (live) {
      const filtered = filterAndSort(live, limit, minValue);
      const payload: DropsResponse = {
        tld: tldRaw,
        count: filtered.length,
        drops: filtered,
        source: "live",
        notes: null,
      };
      CACHE.set(cacheKey, payload);
      return NextResponse.json(payload);
    }

    // ── Stub fallback (current default) ───────────────────────────────────
    const drops = await generateStubDrops(tldRaw, limit);
    const filtered = filterAndSort(drops, limit, minValue);

    const payload: DropsResponse = {
      tld: tldRaw,
      count: filtered.length,
      drops: filtered,
      source: "stub",
      notes:
        "Feed is stubbed pending paid drop-catching integration (DropCatch/SnapNames). Upgrade path documented in route comment.",
    };
    CACHE.set(cacheKey, payload);
    return NextResponse.json(payload);
  } catch (err) {
    const msg = err instanceof Error ? err.message : "Internal server error";
    console.error("[domains/drops] Unexpected error:", msg);
    return NextResponse.json({ error: msg }, { status: 500 });
  }
}

