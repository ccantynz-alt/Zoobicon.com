function filterAndSort(drops: DropItem[], limit: number, minValue: number): DropItem[] {
  return drops
    .filter((d) => d.predicted_value.midpoint >= minValue)
    .sort((a, b) => b.predicted_value.midpoint - a.predicted_value.midpoint)
    .slice(0, limit);
}

function clampInt(v: string | null, fallback: number, min: number, max: number): number {
  if (v === null) return fallback;
  const n = Number(v);
  if (!Number.isFinite(n)) return fallback;
  return Math.max(min, Math.min(Math.floor(n), max));
}

function isoDaysFromNow(days: number): string {
  const d = new Date(Date.now() + days * 24 * 60 * 60 * 1000);
  return d.toISOString().slice(0, 10);
}

function normaliseStatus(v: unknown, days: number): DropStatus {
  if (typeof v === "string") {
    const s = v.trim();
    if (s === "grace" || s === "redemption" || s === "pendingDelete" || s === "dropping") {
      return s;
    }
  }
  // Fallback inferred from timing: ICANN grace ~0-30d, redemption ~30-60d,
  // pendingDelete ~last 5d, dropping ~last 2d. Our stub uses a compressed
  // timeline so the mapping below is the practical one for UI sorting.
  if (days <= 2) return "dropping";
  if (days <= 5) return "pendingDelete";
  if (days <= 14) return "redemption";
  return "grace";
}

