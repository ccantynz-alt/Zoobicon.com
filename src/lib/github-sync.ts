/**
 * Poll until the newly created repo is ready (has a default branch).
 * GitHub can take 1-3 seconds to initialize after creation.
 */
async function waitForRepoReady(
  owner: string,
  repoName: string,
  token: string,
  maxAttempts = 10
): Promise<void> {
  for (let i = 0; i < maxAttempts; i++) {
    const res = await ghFetch(
      `/repos/${owner}/${repoName}/git/refs/heads/main`,
      token
    );
    if (res.ok) return;
    await new Promise((r) => setTimeout(r, 1000));
  }
  throw new Error("Repository initialization timed out. Please try again.");
}
